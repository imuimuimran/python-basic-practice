colors = ("Red", "Blue", "Green", "Yellow", "Orange")

# How to access tuple index

print(colors[0]) # Positive Indexing
print(colors[-1]) # Negetive Indexing
print(colors[1:4]) # Positive Range
print(colors[-3:-1]) # Positively Negetive Indexing
print(colors[:0:-1]) # Negetively Negetive Indexing
print(colors[::-1]) # Reverse Indexing

