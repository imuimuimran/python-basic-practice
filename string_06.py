name1 = "Hello World!"

# slicing from strating (inclusive) to to ending (exclusive) index
print(name1[2:9])

print(name1[0:4])
# if we slice from first index like above we can write another way
print(name1[:4])

# in the same way we can slice inputed index to the last
print(name1[6:])

# negetive indexing

# slice from first index to inputted index negetively
print(name1[:-7])
# slicing from strating (inclusive) to to ending (exclusive) index negetively
print(name1[-10:-3])

# slice inputed index to the last negetively
print(name1[-6:])

